public class UserInfo {
    private string name;
    private int age;
    private string email;
    private boolean isActive;

    public UserInfo( string name, int age, string email, boolean isActive){
        this.name = name;
        this.age = age;
        this.email = email;
        this.isActive = isActive;
    }
    public String getname(){
        return name;
    }



}
